export function getStatusColor(code: number): string {
  if (code === 0) return '#EF4444';
  if (code >= 500) return '#EF4444';
  if (code >= 400) return '#F59E0B';
  if (code >= 300) return '#3B82F6';
  if (code >= 200) return '#22C55E';
  return '#6B7280';
}

